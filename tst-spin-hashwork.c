/* Delay some fixed time */
	static void
delay_tsc (unsigned n)
{
	unsigned long long start, current, diff;
	unsigned int aux;
	start = __builtin_ia32_rdtscp (&aux);
	while (1)
	{
		current = __builtin_ia32_rdtscp (&aux);
		diff = current - start;
		if (diff < n)
			pause ();
		else
			break;
	}
}

static void
wait_a_bit (int delay_time)
{
	if (delay_time > 0)
		delay_tsc (delay_time);
}

